const currentLanguage = localStorage.getItem("language") || "sr";

function loadTranslations(page) {

    document.querySelectorAll("[data-i18n]").forEach(element => {

        const key = element.dataset.i18n;

        let text = translations[currentLanguage].nav[key];

        if (!text) {
            text = translations[currentLanguage].footer[key];
        }

        if (!text) {
            text = translations[currentLanguage][page][key];
        }

        if (text) {
            element.textContent = text;
        }



    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
        const key = element.dataset.i18nPlaceholder;

        let text = translations[currentLanguage].nav[key];

        if (!text)
            text = translations[currentLanguage].footer[key];

        if (!text)
            text = translations[currentLanguage].games[key];

        if (text)
            element.placeholder = text;
    });

}

const LANGUAGE_KEY = "language";

window.onload = function () {

    updateLanguageSelector();

    const page = window.location.pathname
        .split("/")
        .pop()
        .replace(".html", "");

    loadTranslations(page);

    if (page !== "item") {
        document.title = translations[getLanguage()][page].title;
    }

    document.getElementById("changeLanguage").addEventListener("click", function (e) {
        e.preventDefault();
        changeLanguage();
    });

};

function updateLanguageSelector() {

    const language = getLanguage();

    const currentFlag = document.getElementById("currentLanguageFlag");
    const currentText = document.getElementById("currentLanguageText");

    const changeFlag = document.getElementById("changeLanguageFlag");
    const changeText = document.getElementById("changeLanguageText");

    if (language === "sr") {

        currentFlag.src = "../img/serbia.png";
        currentFlag.alt = "Srpski";
        currentText.textContent = "SR";

        changeFlag.src = "../img/british.png";
        changeFlag.alt = "English";
        changeText.textContent = "EN";

    } else {

        currentFlag.src = "../img/british.png";
        currentFlag.alt = "English";
        currentText.textContent = "EN";

        changeFlag.src = "../img/serbia.png";
        changeFlag.alt = "Srpski";
        changeText.textContent = "SR";
    }

}

function changeLanguage() {

    const current = getLanguage();
    const newLanguage = current === "sr" ? "en" : "sr";

    setLanguage(newLanguage);

    const page = window.location.pathname
        .split("/")
        .pop()
        .replace(".html", "");

    updateLanguageSelector();
    loadTranslations(page);

    document.title = translations[newLanguage][page].title;
    location.reload();
}

function getLanguage() {
    return localStorage.getItem(LANGUAGE_KEY) || "sr";
}

function setLanguage(language) {
    localStorage.setItem(LANGUAGE_KEY, language);
}



function changeLanguageTo(language) {
    setLanguage(language);
    location.reload();
}

